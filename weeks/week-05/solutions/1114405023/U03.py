# U03. 字串格式化效能與陷阱（2.14–2.20）
# 主題：
# 1. 比較字串用 + 串接與 join() 的效能差異
# 2. 使用 format_map() 時，如何安全處理缺失鍵
# 3. 說明 str 與 bytes 在索引行為上的差異
# 4. 補充 bytes 不能直接做字串格式化的注意事項

import timeit

# --------------------------------------------------
# 一、join 效能優於 +（2.14）
# --------------------------------------------------
# 在 Python 中，字串是「不可變（immutable）」物件。
# 這表示每次做 s += p 時，都不是在原地修改字串，
# 而是建立一個新的字串物件，再把舊內容和新內容複製進去。
#
# 如果在迴圈中反覆使用 + 來串接很多小字串，
# 就會造成大量重複配置與複製，效率會很差。
#
# 相對地，"".join(parts) 會先知道總共要組多少內容，
# 然後一次配置足夠空間，把所有片段接起來，
# 因此通常更快，也更適合大量字串拼接。

# 建立一個清單，內含 1000 個字串：
# ["item0", "item1", "item2", ..., "item999"]
parts = [f"item{i}" for i in range(1000)]

# 不好的做法：
# 在迴圈中不斷用 += 串接字串
def bad_concat():
    # 初始為空字串
    s = ""

    # 每一次 s += p
    # 都可能建立新的字串物件並複製原本內容
    for p in parts:
        s += p  # 每次建立新字串，成本高

    return s

# 較好的做法：
# 直接用 join() 一次把所有字串接起來
def good_join():
    # "".join(parts) 的意思是：
    # 用空字串當分隔符，把 parts 中的所有元素接成一個字串
    return "".join(parts)  # 一次分配，通常更有效率

# 使用 timeit 測試兩種做法的執行時間
# 每個函式執行 500 次
t1 = timeit.timeit(bad_concat, number=500)
t2 = timeit.timeit(good_join, number=500)

# 印出效能比較結果
# 通常 join() 會比 + 串接更快
print(f"+串接: {t1:.3f}s  join: {t2:.3f}s")


# --------------------------------------------------
# 二、format_map 處理缺失鍵（2.15）
# --------------------------------------------------
# 一般來說，若字串模板中的欄位名稱不存在，
# 使用 format() / format_map() 時會拋出 KeyError。
#
# 例如：
# "{name} has {n} messages.".format_map({"name": "Guido"})
# 因為 n 不存在，就會報錯。
#
# 這裡透過自訂 dict 類別，覆寫 __missing__ 方法，
# 讓缺失的鍵不要報錯，而是原樣保留佔位符。

class SafeSub(dict):
    # 當查詢的 key 不存在時，dict 會自動呼叫 __missing__()
    def __missing__(self, key: str) -> str:
        # 把缺失的欄位保留成原本的樣子，例如 {n}
        # 這樣格式化時就不會報錯，也方便後續再補值
        return "{" + key + "}"

# 定義一個變數 name
name = "Guido"

# 建立模板字串
# 其中 {name} 有值，但 {n} 尚未定義
s = "{name} has {n} messages."

# vars() 會回傳目前區域變數組成的字典
# 例如此處可能包含：
# {"name": "Guido", "s": "...", ...}
#
# 再用 SafeSub 包起來後，若模板中有找不到的鍵，
# 就會套用 __missing__()，改成保留原樣而不是報錯
print(s.format_map(SafeSub(vars())))
# 輸出：
# 'Guido has {n} messages.'
#
# 說明：
# {name} 被成功替換成 Guido
# {n} 因為不存在，所以被保留為 {n}


# --------------------------------------------------
# 三、bytes 索引回傳整數（2.20）
# --------------------------------------------------
# Python 3 中，str 與 bytes 是不同型別：
#
# str   -> 文字字串（Unicode）
# bytes -> 位元組序列（二進位資料）
#
# 它們在索引時的行為不同：
# str[index]   會回傳「字元」
# bytes[index] 會回傳「整數」（該位元組的數值 0~255）

# 一般字串（str）
a = "Hello"

# 位元組字串（bytes）
# 前面加 b 表示這不是文字字串，而是 bytes
b = b"Hello"

# str 索引回傳的是字元
print(a[0])  # 'H'

# bytes 索引回傳的是整數
# 因為 bytes 本質上是一串位元組
# 'H' 的 ASCII / Unicode 編碼值是 72
print(b[0])  # 72

# 補充理解：
# ord('H') == 72
# chr(72) == 'H'


# --------------------------------------------------
# 四、bytes 不能直接 format，需先格式化再 encode
# --------------------------------------------------
# bytes 物件不像 str 那樣支援一般字串格式化方法。
# 所以通常正確流程是：
#
# 1. 先建立普通字串（str）
# 2. 用 format() 完成格式化
# 3. 最後再 encode 成 bytes
#
# {:10s} 表示字串欄位寬度至少 10
# {:5d}  表示整數欄位寬度至少 5
#
# "ACME" 會靠左放入 10 格
# 100    會放入 5 格中
#
# 最後 encode("ascii") 轉成 ASCII bytes
print("{:10s} {:5d}".format("ACME", 100).encode("ascii"))
# 輸出：
# b'ACME            100'
#
# 注意：
# 輸出前面的 b 表示這是一個 bytes 物件，不是普通 str
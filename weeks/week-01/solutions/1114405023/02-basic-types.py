# 檔案：基本數據類型練習
# 學習Python的基本數據類型（int、float、str、bool）

# 整數型
x = 7
# 浮點數型
y = 3.5
# 字符串型
name = "ACME"
# 布爾型
ok = True

# 字符串轉換為整數
s = '12'
num = int(s)
# 字符串轉換為浮點數
price = float('19.99')

# 使用type()函數查看變量的數據類型
print(type(x))  # <class 'int'>
print(type(y))  # <class 'float'>
print(type(ok))  # <class 'bool'>
print(type(name))  # <class 'str'>
print(type(num))  # <class 'int'>
print(type(price))  # <class 'float'>